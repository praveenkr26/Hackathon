export const t = (language, key) => {
  const keys = key.split('.');
  let value = translations[language] || translations.en;
  for (const k of keys) {
    value = value?.[k];
    if (value === undefined) {
      // Fallback to English
      let fallback = translations.en;
      for (const fk of keys) fallback = fallback?.[fk];
      return fallback || key;
    }
  }
  return value;
};

